#Write a python program to implement python mathematical functions.
import math
a=4.5
b=math.ceil(a)
c=math.floor(a)
d=-23
e=math.fabs(d)
f=5
g=math.factorial(f)
l=math.log(f)
p=math.pow(25,2)
x=60
s=math.sin(x)
y=math.cos(x)
t=math.tan(x)
i=math.pi
print("ceil of",a,"is:",b)
print("floor of",a,"is:",c)
print("fabs of",d,"is:",e)
print("factorial of",f,"is:",g)
print("log to the base e",f,"is:",l)
print("25 raised to 2 is:",p)
print("sine of",x,"is:",s)
print("cosine of",x,"is:",y)
print("tangent of",x,"is:",t)
print("Value of pi is:",i)
